package vo;

public class PassingRateVO {
	

    private String passRate2020;
    private String passRate2021;
    private String passRate2022;
    
	public PassingRateVO(String passRate2020, String passRate2021, String passRate2022) {
		super();
		this.passRate2020 = passRate2020;
		this.passRate2021 = passRate2021;
		this.passRate2022 = passRate2022;
	}

	public String getPassRate2020() {
		return passRate2020;
	}

	public void setPassRate2020(String passRate2020) {
		this.passRate2020 = passRate2020;
	}

	public String getPassRate2021() {
		return passRate2021;
	}

	public void setPassRate2021(String passRate2021) {
		this.passRate2021 = passRate2021;
	}

	public String getPassRate2022() {
		return passRate2022;
	}

	public void setPassRate2022(String passRate2022) {
		this.passRate2022 = passRate2022;
	}

	public PassingRateVO() {
		super();
	}
	
	
	
    
    

}
